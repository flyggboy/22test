.section .text
.syntax unified

.global aes_encrypt

aes_encrypt:
    push {r4, r5, r6, r7, lr}

    ldr r4, [r0]        @ r4 = plaintext[0]
    ldr r5, [r0, #4]    @ r5 = plaintext[4]
    ldr r6, [r1]        @ r6 = key[0]
    ldr r7, [r1, #4]    @ r7 = key[4]

    @ Perform AddRoundKey
    eor r4, r4, r6
    eor r5, r5, r7

    @ Perform SubBytes
    bl sub_bytes

    @ Perform ShiftRows
    bl shift_rows

    @ Perform MixColumns
    bl mix_columns

    @ Perform AddRoundKey
    ldr r6, [r1, #8]    @ r6 = key[8]
    ldr r7, [r1, #12]   @ r7 = key[12]
    eor r4, r4, r6
    eor r5, r5, r7

    @ Store the encrypted data
    str r4, [r2]
    str r5, [r2, #4]

    pop {r4, r5, r6, r7, pc}


sub_bytes:
    @ SubBytes lookup table
    ldr r3, =sbox

    @ SubBytes for r4
    ldrb r0, [r4]
    ldr r1, [r3, r0]
    mov r4, r1

    @ SubBytes for r5
    ldrb r0, [r5]
    ldr r1, [r3, r0]
    mov r5, r1

    bx lr


shift_rows:
    @ ShiftRows for r4
    lsr r0, r4, #8
    lsl r1, r4, #24
    orr r4, r0, r1

    @ ShiftRows for r5
    lsr r0, r5, #16
    lsl r1, r5, #16
    orr r5, r0, r1

    @ Rotate r5 to the right by 8 bits
    ror r5, r5, #8

    bx lr


mix_columns:
    @ MixColumns for r4
    push {r0-r3}
    mov r0, r4
    lsr r1, r4, #8
    lsl r2, r4, #16
    lsr r3, r4, #24
    eor r4, r0, r1
    eor r4, r4, r2
    eor r4, r4, r3
    eor r4, r4, lsr #31
    eors r4, r4
    pop {r0-r3}

    @ MixColumns for r5
    push {r0-r3}
    mov r0, r5
    lsl r1, r5, #8
    lsr r2, r5, #16
    lsl r3, r5, #24
    eor r5, r0, r1
    eor r5, r5, r2
    eor r5, r5, r3
    eor r5, r5, lsr #31
    eors r5, r5
    pop {r0-r3}

    bx lr


